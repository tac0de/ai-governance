function detectDefaultLanguage() {
  const supported = ["en", "ko", "ja", "zh"];
  const stored = localStorage.getItem("showcase_lang");
  if (stored && supported.includes(stored)) return stored;
  const browser = (navigator.language || "en").slice(0, 2).toLowerCase();
  return supported.includes(browser) ? browser : "en";
}

function setText(lang) {
  const selected = copy[lang] || copy.en;
  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.getAttribute("data-i18n");
    if (key && selected[key]) node.textContent = selected[key];
  });

  fillList("pillars-list", selected.pillars);
  fillList("roles-list", selected.roles);
  fillList("automation-list", selected.automation);
  fillBlog(lang);
  attachRepoLinks(lang);
}

function fillList(id, values) {
  const list = document.getElementById(id);
  list.innerHTML = "";
  values.forEach((v) => {
    const li = document.createElement("li");
    li.textContent = v;
    list.appendChild(li);
  });
}

function fillBlog(lang) {
  const list = document.getElementById("blog-list");
  const data = notes[lang] || notes.en;
  list.innerHTML = "";
  data.forEach((item) => {
    const card = document.createElement("article");
    card.className = "blog-item";
    const title = document.createElement("h3");
    title.textContent = `${item.date} | ${item.title}`;
    const body = document.createElement("p");
    body.textContent = item.body;
    card.appendChild(title);
    card.appendChild(body);
    list.appendChild(card);
  });
}

function repoBaseUrl() {
  const host = window.location.hostname;
  if (!host.endsWith("github.io")) return "";
  const owner = host.split(".")[0];
  const repo = window.location.pathname.split("/").filter(Boolean)[0] || "ai-governance";
  return `https://github.com/${owner}/${repo}/blob/main`;
}

function attachRepoLinks(lang) {
  const root = repoBaseUrl();
  const labels = copy[lang] || copy.en;
  const linkMap = [
    { id: "readme-link", path: "README.md", text: labels.readmeLink },
    { id: "services-registry-link", path: "control/registry/services.v0.1.json", text: labels.servicesRegistryLink },
    { id: "mcps-registry-link", path: "control/registry/mcps.v0.1.json", text: labels.mcpsRegistryLink }
  ];

  linkMap.forEach((link) => {
    const el = document.getElementById(link.id);
    el.textContent = link.text;
    if (root) {
      el.href = `${root}/${link.path}`;
      el.target = "_blank";
      el.rel = "noreferrer";
    } else {
      el.href = "#";
    }
  });
}

async function sha256(text) {
  const bytes = new TextEncoder().encode(text);
  const hash = await crypto.subtle.digest("SHA-256", bytes);
  return [...new Uint8Array(hash)].map((b) => b.toString(16).padStart(2, "0")).join("");
}

